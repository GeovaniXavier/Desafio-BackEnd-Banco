package com.geovani.dsf.dto;

public class TransferenciaDto {

    private Long de;

    private Long para;

    private Double valor;

    public Long getDe() {
        return de;
    }

    public void setDe(Long de) {
        this.de = de;
    }

    public Long getPara() {
        return para;
    }

    public void setPara(Long para) {
        this.para = para;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }
}
